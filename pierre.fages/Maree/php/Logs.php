<?php
		//------------------------------------------------------
		// Les variables privees
		//------------------------------------------------------
		$debug = TRUE;
		$internalnomlog = '';
		
		
		//==========================================================================================
		// les methodes
		//==========================================================================================
		function Add($msg)
		{
			if ($debug)
			{
				$handle = fopen("./logs.txt", "a");
				fprintf($handle, '%s'.PHP_EOL, '['.date('y/m/d h:m:s:u').'-'.$this->internalnomlog.' ]'.$msg);
				fclose ($handle);
			}
		}	
?>